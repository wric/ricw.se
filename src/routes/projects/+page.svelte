<h3>Is bolaget closed</h3>
<p>
	You know, I live in Sweden where we have the alcohol monopoly owned by the government. Without
	aspiring to be any kind of political blogger, I can conclude that it has pros and cons. One con is
	that it has limited opening hours. As a Swede you learn it in and out, and often assumes the
	availability of alcohol. But some old holidays that no one cares about sometime leads to deviating
	opening hours that are <strong>really</strong> easy to forget!
</p>

<p>
	<i>Is bolaget closed</i> is both a site for easeli browes your favorite stores opening hours (they
	do vary). But also provides a API for querying if the opening hourse deviates. Put the query on a automation
	with some notification when deviating, and you will never stay unwantedly sober.
</p>
